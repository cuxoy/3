package lesson3;

public class MathClass {
   public static int Add  (int a, int b,int c) {
        return a + b + c;



    }
    static long Sub (long a, long b, long c) {
        return a - b - c;

   }
   double Mult (double a, double b, double c ) {
       return a * b * c;

   }
   public double Div (int a, long b, double c){
       return a / b / c;

   }
}
